package com.wyzk.lottery.model;

import java.io.Serializable;
import java.util.List;


public class IntegralRecordModel implements Serializable {
    private int total;
    private int records;
    private int page;
    private List<RecordModel> rows;

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }

    public int getRecords() {
        return records;
    }

    public void setRecords(int records) {
        this.records = records;
    }

    public int getPage() {
        return page;
    }

    public void setPage(int page) {
        this.page = page;
    }

    public List<RecordModel> getRows() {
        return rows;
    }

    public void setRows(List<RecordModel> rows) {
        this.rows = rows;
    }

    public class RecordModel {
        private String roomName;
        private int roomId;
        private int roundId;
        private int betValue;
        private String betTime;
        private int winLoseValue;
        private int betId;

        public String getRoomName() {
            return roomName;
        }

        public void setRoomName(String roomName) {
            this.roomName = roomName;
        }

        public int getRoomId() {
            return roomId;
        }

        public void setRoomId(int roomId) {
            this.roomId = roomId;
        }

        public int getRoundId() {
            return roundId;
        }

        public void setRoundId(int roundId) {
            this.roundId = roundId;
        }

        public int getBetValue() {
            return betValue;
        }

        public void setBetValue(int betValue) {
            this.betValue = betValue;
        }

        public String getBetTime() {
            return betTime;
        }

        public void setBetTime(String betTime) {
            this.betTime = betTime;
        }

        public int getWinLoseValue() {
            return winLoseValue;
        }

        public void setWinLoseValue(int winLoseValue) {
            this.winLoseValue = winLoseValue;
        }

        public int getBetId() {
            return betId;
        }

        public void setBetId(int betId) {
            this.betId = betId;
        }
    }
}
